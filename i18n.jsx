const LangContext = React.createContext({ lang: "ko", setLang: () => {}, t: (k) => k });

function detectLang() {
  try {
    const saved = localStorage.getItem("tikke_lang");
    if (saved && I18N[saved]) return saved;
  } catch {}
  const nav = (typeof navigator !== "undefined" && navigator.language) || "ko";
  if (nav.startsWith("ja")) return "ja";
  if (nav.startsWith("zh")) return "zh";
  if (nav.startsWith("en")) return "en";
  return "ko";
}

function LangProvider({ children }) {
  const [lang, setLangState] = React.useState(detectLang);

  React.useEffect(() => {
    document.documentElement.lang = lang;
  }, [lang]);

  const setLang = React.useCallback((l) => {
    setLangState(l);
    try { localStorage.setItem("tikke_lang", l); } catch {}
  }, []);

  const t = React.useCallback((key) => {
    const dict = I18N[lang] || I18N.ko;
    const v = dict[key];
    return v !== undefined ? v : (I18N.ko[key] !== undefined ? I18N.ko[key] : key);
  }, [lang]);

  return (
    <LangContext.Provider value={{ lang, setLang, t }}>
      {children}
    </LangContext.Provider>
  );
}

function useT() {
  return React.useContext(LangContext);
}

function LangSwitcher() {
  const { lang, setLang } = useT();
  const langs = [
    { code: "ko", label: "KO" },
    { code: "en", label: "EN" },
    { code: "ja", label: "JA" },
    { code: "zh", label: "ZH" },
  ];
  return (
    <div className="lang-switch" role="radiogroup" aria-label="Language">
      {langs.map((l) => (
        <button
          key={l.code}
          type="button"
          role="radio"
          aria-checked={lang === l.code}
          className={"lang-opt" + (lang === l.code ? " active" : "")}
          onClick={() => setLang(l.code)}
        >
          {l.label}
        </button>
      ))}
    </div>
  );
}

window.I18N = I18N;
window.LangProvider = LangProvider;
window.useT = useT;
window.LangSwitcher = LangSwitcher;
